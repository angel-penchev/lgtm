#include <stdio.h>

void принтиПилешка(void);
int сметниБаба(int картофи, int чушки);
void станиБогат(int *банковаСметка);

int main() {
    int бройДоктори = 10;
    int бройКокошки = 5;
    int годиниБаба = 20;
    int касичка = 1000;

    printf("Брой доктори: %d\n", бройДоктори);
    printf("Брой кокошки: %d\n", бройКокошки);

    // Извикване на функции
    принтиПилешка();

    int общоКюфтета = сметниБаба(бройДоктори, бройКокошки);
    printf("Общо кюфтета: %d\n", общоКюфтета);

    станиБогат(&касичка);
    printf("Касичка след станиБогат: %d\n", касичка);

    // Цикъл, който симулира пиян българин, броящ бутилките си с ракия
    for (int бройкаРакия = 1; бройкаРакия <= 5; бройкаРакия++) {
        printf("Бутилка ракия %d: Колко много мръсно!\n", бройкаРакия);
    }

    // Условно изявление за проверка на възрастта на баба
    if (годиниБаба > 18) {
        printf("Баба може да пие ракия!\n");
    } else {
        printf("Баба е твърде млада за ракия!\n");
    }

    return 0;
}

// Функция за отпечатване на нещо свързано с пилета
void принтиПилешка(void) {
    printf("Пиле: Къде си? Къде си?\n");
}

// Функция за изчисляване на общия брой кюфтета, които баба е направила
int сметниБаба(int картофи, int чушки) {
    int общоКюфтета = картофи * чушки;
    return общоКюфтета;
}

// Функция за увеличаване на банковата сметка, сякаш печелите игрално шоу
void станиБогат(int *банковаСметка) {
    *банковаСметка += 5000; // Добавени 5000 лева
    printf("Това е Стани богат! Спечелихте 5000 лева!\n");
}